//Q11.) Write a code in C to take two numbers and compare them as integers.

#include <stdio.h>

int main() {

    int a = 10, b = 5;
    printf("a == b, b:%d\n", a==b);
    printf("a != b, b:%d\n", a!=b);
    printf("a > b; b:%d\n", a>b);
    printf("a < b; b:%d\n", a<b);
    printf("a >= b; b:%d\n", a>=b);
    printf("a <= b; b:%d\n", a<=b);



    return 0;
    
}